          numo1=Double.parseDouble(t112.getText());
          numo2=Double.parseDouble(t113.getText());
          numo3=Double.parseDouble(t114.getText());
          numo4=Double.parseDouble(t115.getText());

          double sn1 = Math.toRadians(numo1);
             numo1 = (Math.sin(sn1));
